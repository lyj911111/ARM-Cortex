/* Thread1 function */
void Thread1(void const * argument)				//	자동생성된 쓰레드 원형 함수.
{

  /* USER CODE BEGIN 5 */
	uint8_t txData[20] = "Hello from Thread1\r\n";		//	** 쓰레드 마다 공통으로 사용할 버퍼 **
  /* Infinite loop */
  for(;;)
  {
	  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_7);

	  xSemaphoreTake(uartMutexHandle, portMAX_DELAY);		//	** osMutexID(위에 선언한 ID)를 이곳에 입력, 세마포어를 가져가 다른 쓰레드가 접근하지 못하도록 막음. (충돌을 막음)**
	  HAL_UART_Transmit(&huart3, txData, 20, 5 );			//	데이터 출력.
	  xSemaphoreGive(uartMutexHandle);						//	** osMutexID(위에 선언한 ID)를 이곳에 입력, 세마포어를 내려 놓고 다른 쓰레드가 쓸 수 있도록 함. **
	  osDelay(1000);
  }
  /* USER CODE END 5 */ 
}

/* Thread2 function */
void Thread2(void const * argument)
{
  /* USER CODE BEGIN Thread2 */
	uint8_t txData[20] = "Hello from Thread2\r\n";
  /* Infinite loop */
  for(;;)
  {
	  if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_SET)	//	스위치가 눌릴 경우 접근할 수 있도록,
	  {
		  xSemaphoreTake(uartMutexHandle, portMAX_DELAY);		//	** Queue(대기열)에 추가되어 쓸 수 있는 세마포어가 있는 경우 가져가서, 다른 쓰레드가 접근하지 못하도록 막음 (이제 본인만 쓸 수 있도록)**
		  HAL_UART_Transmit(&huart3, txData, 20, 5 );
		  xSemaphoreGive(uartMutexHandle);						//	**값을 출력후 다시 세마포어를 내려놓음**
	  }
	  osDelay(1000);
  }
  /* USER CODE END Thread2 */
}

