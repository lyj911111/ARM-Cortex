/* RotationTask function */
void RotationTask(void const * argument)
{
  /* USER CODE BEGIN RotationTask */

  /* Infinite loop */
  for(;;)
  {

	  HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, 500);
	  adc1_value = HAL_ADC_GetValue(&hadc1);
	  HAL_ADC_Stop(&hadc1);

	  xSemaphoreTake(uartMutexHandle, portMAX_DELAY);				//	** 세마포어를 가져가고, 변수를 사용함. (최고 우선권을 지니게 되며 다른 쓰레드는 변수에 접근하지 못하게 막음)**
	  memset(uart_buf1, 0, sizeof(uart_buf1));
	  sprintf(uart_buf1, "Rotation : %d\r\n", adc1_value);
	  HAL_UART_Transmit(&huart3, uart_buf1, sizeof(uart_buf1), 5);	//	변수값을 출력
	  xSemaphoreGive(uartMutexHandle);								//	**	세마포어를 내려놓아 이제 다른 변수도 접근할 수 있음 (사용을 다했으니, 우선권을 내려 놓음)**

	  osDelay(1000);
  }
  /* USER CODE END RotationTask */
}

